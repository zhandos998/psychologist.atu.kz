    private function seedPsychologicalStress(?User $admin): void
    {
        $test = $this->freshTest($admin, [
            'title' => 'Шкала психологического стресса',
            'description' => 'Оценка уровня стресса студентов по соматическим, поведенческим и эмоциональным показателям. Шкала Лемур-Тесье-Филлион, адаптация Н.Е. Водопьяновой.',
            'type' => 'psychology',
            'category' => 'Аутодеструктивное поведение',
            'is_required' => true,
        ]);

        foreach ($this->stressQuestions() as $index => $text) {
            $isReverseQuestion = $index + 1 === 14;
            $question = $this->question($test, $text, 'scale', $index + 1);

            foreach (
                [
                    ['1 - никогда', 1],
                    ['2 - очень редко', 2],
                    ['3 - редко', 3],
                    ['4 - иногда редко', 4],
                    ['5 - иногда', 5],
                    ['6 - часто', 6],
                    ['7 - очень часто', 7],
                    ['8 - постоянно', 8],
                ] as [$label, $score]
            ) {
                $this->option($question, $label, $isReverseQuestion ? 9 - $score : $score, (string) $score);
            }
        }

        $test->scoringRule()->create(['method' => 'simple_sum', 'config' => []]);

        $this->interpret($test, null, 25, 99, 'Низкий уровень стресса', 'Хорошая психологическая адаптация и низкий уровень напряжения.', false);
        $this->interpret($test, null, 100, 155, 'Средний уровень стресса', 'Умеренная напряженность, адаптация в целом сохранена.', false);
        $this->interpret($test, null, 156, 200, 'Высокий уровень стресса', 'Выраженная психическая напряженность и эмоциональный дискомфорт.', true);

        $this->applyKazTranslations($test, 'stress');
    }

